# SELECT: Seleciona dados de uma ou mais tabelas.
# INSERT: Insere novos dados em uma tabela.
# UPDATE: Atualiza dados existentes em uma tabela.
# DELETE: Remove dados de uma tabela.
# CREATE: Cria novas tabelas, índices ou outros objetos de banco de dados.
# ALTER: Modifica a estrutura de uma tabela existente.
# DROP: Remove uma tabela, índice ou outro objeto de banco de dados.
# TRUNCATE: Remove todos os dados de uma tabela.
# JOIN: Combina dados de duas ou mais tabelas com base em uma condição de junção.
# GROUP BY: Agrupa dados com base em uma ou mais colunas e aplica funções agregadas a esses grupos.
# HAVING: Restringe os resultados de uma consulta agrupada a um subconjunto de grupos com base em uma condição.
# ORDER BY: Classifica os resultados de uma consulta em ordem ascendente ou descendente com base em uma ou mais colunas.
# LIMIT: Limita o número de resultados retornados por uma consulta.
# OFFSET: Especifica o número de linhas a serem ignoradas no início de uma consulta.
# UNION: Combina os resultados de duas ou mais consultas em uma única lista de resultados.
# INTERSECT: Retorna apenas as linhas que aparecem em ambas as consultas.
# EXCEPT: Retorna apenas as linhas que aparecem na primeira consulta, mas não na segunda consulta.


# INT: tipo de dados utilizado para armazenar números inteiros, como 1, 2, 3, -4, -5, etc.

# FLOAT: tipo de dados utilizado para armazenar números decimais, como 1.5, 2.25, -3.75, etc.

# CHAR: tipo de dados utilizado para armazenar strings de tamanho fixo, ou seja, strings com um número específico de
# caracteres. Por exemplo, um campo CHAR(10) pode armazenar uma string de até 10 caracteres.

# VARCHAR: tipo de dados utilizado para armazenar strings de tamanho variável, ou seja, strings com um número
# variável de caracteres. Por exemplo, um campo VARCHAR(255) pode armazenar uma string de até 255 caracteres.

# DATE: tipo de dados utilizado para armazenar datas, como 2023-03-31.

# TIME: tipo de dados utilizado para armazenar horários, como 12:30:45.

# DATETIME: tipo de dados utilizado para armazenar datas e horários, como 2023-03-31 12:30:45.

# BOOLEAN: tipo de dados utilizado para armazenar valores lógicos, como TRUE ou FALSE.

# BLOB: tipo de dados utilizado para armazenar grandes quantidades de dados binários, como imagens ou arquivos.
